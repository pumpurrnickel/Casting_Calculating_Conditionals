public class Casting {
    public static void main(String[] args) {

        double doub = 112.35;
        int numb = (int)doub;

        System.out.println("Double: " + doub);
        System.out.println("Int: " + numb);

        String text = "49";
        int num = Integer.parseInt(text);

        System.out.println("The string value is: " + text);
        System.out.println("The interger value is: " + num);


    }
}
